// Function to get the correct sprite name for an item
export function getItemSpriteName(internalName: string): string {
  // First check if there's a manual mapping
  if (itemSpriteMapping[internalName]) {
    return itemSpriteMapping[internalName];
  }
  
  // Otherwise apply the standard transformation
  let spriteName = internalName
    .toLowerCase()
    // Split camelCase or consecutive capitals
    .replace(/([a-z])([A-Z])/g, '$1-$2')
    .replace(/([A-Z]+)([A-Z][a-z])/g, '$1-$2')
    .toLowerCase();
  
  // Don't apply suffixes if they're already hyphenated
  if (!spriteName.includes('-')) {
    // Handle specific suffixes
    const suffixes = [
      'ball', 'berry', 'stone', 'potion', 'mail', 'plate', 'drive', 'gem',
      'shard', 'flute', 'repel', 'rope', 'heal', 'restore', 'elixir', 'ether',
      'powder', 'herb', 'root', 'candy', 'capsule', 'machine'
    ];
    
    for (const suffix of suffixes) {
      if (spriteName.endsWith(suffix) && spriteName !== suffix) {
        spriteName = spriteName.slice(0, -suffix.length) + '-' + suffix;
        break;
      }
    }
  }
  
  // Fix double hyphens
  spriteName = spriteName.replace(/--/g, '-');
  
  // Remove trailing hyphen if exists
  spriteName = spriteName.replace(/-$/, '');
  
  return spriteName;
}